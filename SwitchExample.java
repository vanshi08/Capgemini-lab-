package co.vanshika;

public class SwitchExample {
public static void main(String args[]) {
        
        int x = 6;
        //float y = 3.2f;   // Java considers any decimal number as a double value by default
        
        switch(x) {
        
        
       
            
        case 1: 
            System.out.println("1");
            break;
        case 2:
            System.out.println("2");
            break;
            
        case 3:
            System.out.println("5");
            break;
        default:
            System.out.println("default");
           break;
            
        }
        
        
        
        
    } 
    
}

